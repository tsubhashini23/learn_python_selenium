# while loop:
# Repeats as long as a condition is True.

# for loop:
# Iterates over a sequence like a list, tuple, string, or range()

fruits = ['apple', 'banana', 'mango']

# using for loop
for fruit in fruits:
    print(fruit)

# using while loop
i = 0
while i < len(fruits):
    print(fruits[i])
    i += 1